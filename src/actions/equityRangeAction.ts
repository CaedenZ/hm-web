export const createJobGradeAction = createAsyncAction(
    CREATE_JOBGRADE_REQUEST,
    CREATE_JOBGRADE_SUCCESS,
    CREATE_JOBGRADE_FAILURE
)<CREATEJOBGRADECRED, void, void>()

const UPDATE_JOBGRADE_REQUEST = "UPDATE_JOBGRADE_REQUEST"
const UPDATE_JOBGRADE_SUCCESS = "UPDATE_JOBGRADE_SUCCESS"
const UPDATE_JOBGRADE_FAILURE = "UPDATE_JOBGRADE_FAILURE"
export const updateJobGradeAction = createAsyncAction(
    UPDATE_JOBGRADE_REQUEST,
    UPDATE_JOBGRADE_SUCCESS,
    UPDATE_JOBGRADE_FAILURE
)<UPDATEJOBGRADECRED, void, void>()

const DELETE_JOBGRADE_REQUEST = "DELETE_JOBGRADE_REQUEST"
const DELETE_JOBGRADE_SUCCESS = "DELETE_JOBGRADE_SUCCESS"
const DELETE_JOBGRADE_FAILURE = "DELETE_JOBGRADE_FAILURE"
