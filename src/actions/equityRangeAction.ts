import { createAsyncAction, createAction } from "typesafe-actions";
import { JobGrade, CREATEJOBGRADECRED, UPDATEJOBGRADECRED } from "../interface/jobgradeInterface";

const SELECT_JOBGRADE = "SELECT_JOBGRADE"
export const selectJobGradeAction = createAction(SELECT_JOBGRADE,
    action => {
        return (jobgrade: JobGrade) => action(jobgrade)
    })

const GET_JOBGRADE_LIST_REQUEST = "GET_JOBGRADE_LIST_REQUEST"
const GET_JOBGRADE_LIST_SUCCESS = "GET_JOBGRADE_LIST_SUCCESS"
const GET_JOBGRADE_LIST_FAILURE = "GET_JOBGRADE_LIST_FAILURE"
export const getJobGradeListAction = createAsyncAction(
    GET_JOBGRADE_LIST_REQUEST,
    GET_JOBGRADE_LIST_SUCCESS,
    GET_JOBGRADE_LIST_FAILURE
)<void, JobGrade[], void>()

const CREATE_JOBGRADE_REQUEST = "CREATE_JOBGRADE_REQUEST"
const CREATE_JOBGRADE_SUCCESS = "CREATE_JOBGRADE_SUCCESS"
